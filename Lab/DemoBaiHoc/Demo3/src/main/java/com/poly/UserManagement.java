    private void tblStaffMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblStaffMouseClicked
        // TODO add your handling code here:
        int index = tblStaff.getSelectedRow();
        model = (DefaultTableModel) tblStaff.getModel();
        txtUername.setText(model.getValueAt(index, 0).toString());
        txtPassword.setText(model.getValueAt(index, 1).toString());
         String role =model.getValueAt(index,2).toString();
         if (role.equals("Admin")) {
            rdoAdmin.setSelected(true);
        }else{
             rdoAdmin.setSelected(false);
         }
         tabStaff.setSelectedIndex(0);
    }//GEN-LAST:event_tblStaffMouseClicked

