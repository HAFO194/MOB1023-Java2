//    To chuc cac ham nguoi dung
    private void fillTable() {
        model = (DefaultTableModel) tblStaff.getModel();
        model.setRowCount(0);
        for (User u : lstUser) {
           Object[] row = {u.getUsername(), u.getPassword(),u.getRole()}; 
           model.addRow(row);
        }
    }
    
    void clearForm(){
        txtUername.setText("");
        txtPassword.setText("");
        rdoUser.setSelected(true);
    } 
    
    User readForm(){
        //        Doc du lieu tu form va tao nguoi dung
        String user = txtUername.getText();
        String pass = txtPassword.getText();
        String role = "";
        if (rdoAdmin.isSelected()) {
            role = "Admin";
        } else {
            role = "User";
        }
        User u = new User(user, pass, role);
        return u;
    }
